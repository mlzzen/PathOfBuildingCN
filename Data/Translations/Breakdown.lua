breakdown_translator.patterns = {
    ["Affected By(.*)"] = "受【%s】影响时",
}

function breakdown_translator.Translate(input)
	if input == nil then
		return input
	end
    local pattern_output = nil
	for pattern, patternVal in pairs(breakdown_translator.patterns) do
        local res = input:match(pattern)
        if res ~= nil then
            pattern_output = string.format(patternVal, res)
        end
	end
    if pattern_output ~= nil then
        return pattern_output
    end
	local output = breakdown_translator[input]
	if output ~= nil then
		return output
	else
		return input
	end
end

return breakdown_translator